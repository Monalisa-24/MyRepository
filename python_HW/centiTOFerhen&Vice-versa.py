# centigrade to fahrenheit conversion

c = float(input("Enter any number for temperature : "))

fahren = (c * 9 / 5) + 32

print("The fahrenheit number after conversion = ", fahren)

#  fahrenheit to centigrade conversion

f = float(input("Enter any number for temperature : "))

centi = (f - 32) * 5 / 9
print("The centigrade number after conversion = ", centi)


